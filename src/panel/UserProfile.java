    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String fname = jTextField1.getText();
        String lname = jTextField2.getText();
        String username = jTextField3.getText();
        String email = jTextField4.getText();
        String bio = jTextField6.getText();
        ButtonModel gender = buttonGroup1.getSelection();
        String genderId = gender.getActionCommand();

        try {
            Database.executeIUD("UPDATE `user` SET `fname` = '" + fname + "', `lname` = '" + lname + "', `email` = '" + email + "', `bio` = '" + bio + "', `gender_id` = '" + genderId + "' WHERE `username` = '" + username + "'");
            JOptionPane.showMessageDialog(this, "Profile Updated Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
        }
        loaduserDetails();
    }//GEN-LAST:event_jButton3ActionPerformed

