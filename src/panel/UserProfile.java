    private void loaduserDetails() {

        try {
            ResultSet resultSet = Database.executeSearch("SELECT * FROM `user` WHERE `username` = '" + username + "'");
            if (resultSet.next()) {
                jLabel5.setText(resultSet.getString("username"));
                jLabel6.setText(resultSet.getString("fname") + " " + resultSet.getString("lname"));
                jLabel7.setText(resultSet.getString("email"));
                jLabel16.setText(resultSet.getString("bio"));

                jTextField1.setEditable(false);
                jTextField2.setEditable(false);
                jTextField3.setEditable(false);
                jTextField4.setEditable(false);
                jTextField6.setEditable(false);
                jRadioButton1.setEnabled(false);
                jRadioButton2.setEnabled(false);
                jButton3.setVisible(false);

                jTextField1.setText(resultSet.getString("fname"));
                jTextField2.setText(resultSet.getString("lname"));
                jTextField3.setText(resultSet.getString("username"));
                jTextField4.setText(resultSet.getString("email"));
                jTextField6.setText(resultSet.getString("bio"));

                if (resultSet.getString("gender_id").equals("1")) {
                    jRadioButton1.setSelected(true);
                } else if (resultSet.getString("gender_id").equals("2")) {
                    jRadioButton2.setSelected(true);
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

